package com.nipun.legalscale.feature.legalcasehandling.casedetail.land.enums;

public enum LandDocumentType {
    /** Standard transfer / sale deed */
    DEED,
    /** Official survey plan from the Survey Department */
    SURVEY_PLAN,
    /** Certificate of Title issued by the Land Registry */
    TITLE_DEED,
    /** Partition deed splitting land between owners */
    PARTITION_DEED,
    /** Original grant of land by the Crown / State */
    CROWN_GRANT,
    /** Court order relating to the land (e.g. vesting order) */
    COURT_ORDER,
    /** Land tax / assessment document */
    LAND_TAX_DOCUMENT,
    OTHER
}
